package ac.uk.lancs.seal.metric.processor;

public class Metric {
    private String metric;
    private String value;

    public String getMetric() {
        return metric;
    }

    public void setMetric(String metric) {
        this.metric = metric;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
